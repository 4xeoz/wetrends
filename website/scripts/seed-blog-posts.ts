async function seedPosts() {
  try {
    // Find existing admin user
    let user = await prisma.user.findUnique({
      where: { email: "iyad@wetrends.co.uk" },
    });

    if (!user) {
      console.log("Admin user not found. Please run: npx tsx scripts/create-admin.ts");
      process.exit(1);
    }

    console.log(`Found author: ${user.name} (${user.email})`);

    for (const post of posts) {
      // Check if post already exists
      const existing = await prisma.blogPost.findUnique({
        where: { slug: post.slug },
      });

      if (existing) {
        console.log(`Post "${post.title}" already exists. Skipping.`);
        continue;
      }

      await prisma.blogPost.create({
        data: {
          ...post,
          authorId: user.id,
          published: true,
          publishedAt: new Date(),
        },
      });

      console.log(`✅ Created: "${post.title}"`);
    }

    console.log("\n🎉 All blog posts seeded successfully!");
  } catch (error) {
    console.error("Error seeding blog posts:", error);
    process.exit(1);
  } finally {
    await prisma.$disconnect();
  }
}

seedPosts();
